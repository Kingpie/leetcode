/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    int kthSmallest(TreeNode* root, int k) {
        stack<TreeNode*> s;

        while(!s.empty() || root){
            if(root){
                s.push(root);
                root = root->left;
            }else{
                auto node = s.top();
                s.pop();
                k--;
                if(k<=0){
                    return node->val;
                }
                root = node->right;
            }
        }
        return 0;
    }
};
