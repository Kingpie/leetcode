/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    vector<int> postorderTraversal(TreeNode* root) {
        stack<TreeNode*> s1,s2;
        vector<int> ans;
        if(root)
            s1.push(root);

        while(!s1.empty()){
            auto node = s1.top();
            s1.pop();
            s2.push(node);

            if(node->left)
                s1.push(node->left);
            if(node->right)
                s1.push(node->right);
        }
        while(!s2.empty()){
            ans.push_back(s2.top()->val);
            s2.pop();
        }
        return ans;
    }
};